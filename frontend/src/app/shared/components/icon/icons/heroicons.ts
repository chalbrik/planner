// Funkcje pomocnicze do pobierania ikon
export function getIcon(name: string): HeroIcon | undefined {
  // Usuń sufiks wariantu z nazwy
  const baseName = name.replace(/-(?:outline|solid|mini|micro)$/, '');
  return HEROICONS[baseName];
}

export function getIconVariant(name: string): string {
  const match = name.match(/-(?:outline|solid|mini|micro)$/);
  if (match) {
    return match[0].substring(1); // usuń myślnik
  }
  return 'outline'; // domyślny wariant
}

export function getIconSvg(name: string): string | undefined {
  const icon = getIcon(name);
  const variant = getIconVariant(name) as keyof HeroIcon['variants'];

  if (!icon || !icon.variants[variant]) {
    console.warn(`Icon "${name}" with variant "${variant}" not found`);
    return undefined;
  }

  return icon.variants[variant];
}
